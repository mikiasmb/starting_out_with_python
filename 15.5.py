from turtle import *
hideturtle()
bgcolor("black")
pencolor("white")
penup()
goto(0,-10)
pendown()
circle(10)
penup()
goto(0,0)
pendown()
goto(200,0)
write("  EAST")
goto(0,0)
goto(0,200)
write("NORTH")
goto(0,0)
penup()
goto(-230,0)
write("WEST")
goto(-200,0)
pendown()
goto(0,0)
goto(0,-200)
penup()
goto(0,-215)
pendown()
write("South")

done()