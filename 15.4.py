from turtle import *
radius = 50
hideturtle()
bgcolor("black")
speed(10)
pensize(5)
penup()
goto(-100,0)
pendown()
pencolor("blue")
circle(radius)
penup()
goto(20,0)
pendown()
pencolor("white")
circle(radius)
penup()
goto(140,0)
pendown()
pencolor("red")
circle(radius)
penup()
goto(-40.5,-50)
pendown()
pencolor("yellow")
circle(radius)
penup()
goto(80.5,-50)
pendown()
pencolor("green")
circle(radius)
done()
